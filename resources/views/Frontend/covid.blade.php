         <h2 class="title-section"><span class="fg-primary">Que faire pour vous protéger et protéger les autres de la COVID-19?</h2></span>
          <p>
<p>Laissez une distance d’au moins un mètre entre vous et les autres pour réduire le risque d'infection lorsque vous toussez, éternuez ou parlez. </p>
<p>Gardez une distance encore plus grande entre vous et les autres lorsque vous êtes à l'intérieur. Plus vous êtes éloigné, mieux c'est.</p>
<p>Considérez le port du masque comme normal lorsque vous êtes avec d'autres personnes.</p> 
<p>Pour que les masques soient aussi efficaces que possible, il est essentiel de les porter, de les ranger et de les laver ou de les jeter correctement.<p>
<p>Voici, en quelques points essentiels, comment porter un masque :

Lavez-vous les mains avant de mettre votre masque, ainsi qu'avant de l’enlever et après l'avoir fait.
Assurez-vous qu'il couvre à la fois votre nez, votre bouche et votre menton.
Lorsque vous enlevez votre masque, placez-le dans un sac en plastique propre et lavez-le, chaque jour, s’il s’agit d’un masque en tissu ou jetez-le dans un poubelle si c’est un masque médical.
N’utilisez pas de masques avec des valves d’expiration.</p>
 
</p>
<h2 class="title-section"><span class="fg-primary"> Comment rendre votre environnement plus sûr?</h2></span>
          <p>
Évitez les espaces clos, très fréquentés, où vous serez en contact étroit avec d'autres personnes.
Des foyers épidémiques ont été signalés dans les restaurants, les chorales, les cours d'entraînement physique, les boîtes de nuit, les bureaux et les lieux de culte où les gens sont rassemblés dans des espaces clos bondés où ils parlent fort, crient, respirent profondément ou chantent,les risques de contracter la COVID-19 sont plus élevés dans les espaces très fréquentés et insuffisamment ventilés où des personnes infectées passent de longs moments ensemble dans une étroite promiscuité. C'est dans ces environnements que le virus semble se propager le plus efficacement par des gouttelettes respiratoires ou des aérosols, d’où l’importance d’y prendre toutes les précautions.
<p>Si vous retrouvez d'autres personnes, faites-le à l'extérieur. Les rassemblements à l’extérieur sont plus sûrs que les rassemblements à l’intérieur, surtout si les espaces intérieurs sont petits et dépourvus de ventilation venant de l'extérieur.
Évitez les lieux très fréquentés ou les espaces intérieurs, mais si vous ne pouvez pas faire autrement, prenez des précautions :
*Ouvrez une fenêtre.
* Augmentez le volume de la « ventilation naturelle » lorsque vous êtes à l’intérieur.</p>
</p>
